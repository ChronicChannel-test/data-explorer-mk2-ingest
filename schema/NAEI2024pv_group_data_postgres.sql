      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2015) AS f2015,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2016) AS f2016,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2017) AS f2017,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2018) AS f2018,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2019) AS f2019,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2020) AS f2020,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2021) AS f2021,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2022) AS f2022,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2023) AS f2023,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2024) AS f2024
    FROM series_values sv
    WHERE sv.pollutant_id IS NOT NULL
      AND sv.nfr_group_id IN (SELECT id FROM nfr_ids)
      AND sv.source_id IN (SELECT id FROM source_ids)
      AND sv.activity_id IN (SELECT id FROM activity_ids)
    GROUP BY sv.pollutant_id;
  END LOOP;

  TRUNCATE TABLE public."t_Group_Data" RESTART IDENTITY;

  INSERT INTO public."t_Group_Data" (
    "Group_id",
