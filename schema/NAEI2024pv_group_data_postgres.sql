    f2003 numeric(50,29),
    f2004 numeric(43,22),
    f2005 numeric(43,22),
    f2006 numeric(43,22),
    f2007 numeric(43,22),
    f2008 numeric(43,22),
    f2009 numeric(43,22),
    f2010 numeric(43,22),
    f2011 numeric(43,22),
    f2012 numeric(43,22),
    f2013 numeric(43,22),
    f2014 numeric(43,22),
    f2015 numeric(43,22),
    f2016 numeric(43,22),
    f2017 numeric(43,22),
    f2018 numeric(43,22),
    f2019 numeric(43,22),
    f2020 numeric(43,22),
    f2021 numeric(43,22),
    f2022 numeric(43,22),
    f2023 numeric(43,22),
    f2024 numeric(43,22),
    PRIMARY KEY ("Group_id", "Pollutant_id")
  ) ON COMMIT DROP;

  FOR g IN
    SELECT id, "NFRCode", "SourceName", "ActivityName"
    FROM public."naei_global_t_Group"
    ORDER BY id
  LOOP
    WITH
    canonical_series AS (
      SELECT
        min(s.pv_series_id) AS pv_series_id,
        s.pollutant_id,
        s.nfr_group_id,
        s.source_id,
        s.activity_id
      FROM public.naei2024pv_series s
      WHERE s.pollutant_id IS NOT NULL
      GROUP BY s.dataset_file_id, s.pollutant_id, s.nfr_group_id, s.source_id, s.activity_id
    ),
    nfr_tokens AS (
      SELECT NULLIF(btrim(x), '') AS token
      FROM regexp_split_to_table(COALESCE(g."NFRCode", ''), ';') AS x
      WHERE NULLIF(btrim(x), '') IS NOT NULL
    ),
    source_tokens AS (
      SELECT NULLIF(btrim(x), '') AS token
      FROM regexp_split_to_table(COALESCE(g."SourceName", ''), ';') AS x
      WHERE NULLIF(btrim(x), '') IS NOT NULL
    ),
    activity_tokens AS (
      SELECT NULLIF(btrim(x), '') AS token
      FROM regexp_split_to_table(COALESCE(g."ActivityName", ''), ';') AS x
      WHERE NULLIF(btrim(x), '') IS NOT NULL
    ),
    nfr_ids AS (
      SELECT DISTINCT cs.nfr_group_id AS id
      FROM canonical_series cs
      WHERE (g."NFRCode" IS NULL OR btrim(g."NFRCode") = '')
        AND cs.nfr_group_id IS NOT NULL
      UNION
      SELECT DISTINCT n.id
      FROM nfr_tokens t
      JOIN public.naei_global_t_nfrcode n
        ON n.nfr_code = t.token
      WHERE g."NFRCode" IS NOT NULL AND btrim(g."NFRCode") <> ''
    ),
    source_ids AS (
      SELECT DISTINCT cs.source_id AS id
      FROM canonical_series cs
      WHERE (g.id = 1 OR g."SourceName" IS NULL OR btrim(g."SourceName") = '')
        AND (g.id = 1 OR cs.nfr_group_id IN (SELECT id FROM nfr_ids))
        AND cs.source_id IS NOT NULL
      UNION
      SELECT DISTINCT s.id
      FROM source_tokens t
      JOIN public.naei_global_t_sourcename s
        ON s.source_name = t.token
      WHERE g."SourceName" IS NOT NULL AND btrim(g."SourceName") <> ''
    ),
    activity_ids AS (
      SELECT DISTINCT cs.activity_id AS id
      FROM canonical_series cs
      WHERE (g.id = 1 OR g."ActivityName" IS NULL OR btrim(g."ActivityName") = '')
        AND (g.id = 1 OR cs.source_id IN (SELECT id FROM source_ids))
        AND cs.activity_id IS NOT NULL
      UNION
      SELECT DISTINCT a.id
      FROM activity_tokens t
      JOIN public.naei_global_t_activityname a
        ON a.activity_name = t.token
      WHERE g."ActivityName" IS NOT NULL AND btrim(g."ActivityName") <> ''
    ),
    series_values AS (
      SELECT
        cs.pollutant_id,
        cs.nfr_group_id,
        cs.source_id,
        cs.activity_id,
        v.reporting_year,
        v.metric_value::numeric AS metric_value
      FROM canonical_series cs
      JOIN public.naei2024pv_values v
        ON v.pv_series_id = cs.pv_series_id
       AND v.metric_label = 'value'
       AND v.reporting_year BETWEEN 1970 AND 2024
    )
    INSERT INTO tmp_group_buffer (
      "Group_id",
      "Pollutant_id",
      f1970, f1971, f1972, f1973, f1974, f1975, f1976, f1977, f1978, f1979,
