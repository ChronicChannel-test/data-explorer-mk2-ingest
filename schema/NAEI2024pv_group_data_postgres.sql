-- Compatibility alias for existing callers using the old MySQL-style name.
DROP PROCEDURE IF EXISTS public.insert_23dsgroup_data();
CREATE OR REPLACE PROCEDURE public.insert_23dsgroup_data()
LANGUAGE plpgsql
AS $$
BEGIN
  CALL public.insert_24pvgroup_data();
END;
$$;

-- Read-only exposure for anon/authenticated; full manage for service_role.
ALTER TABLE public."t_Group_Data" ENABLE ROW LEVEL SECURITY;
ALTER TABLE public."t_Group_Data" FORCE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS t_group_data_read ON public."t_Group_Data";
CREATE POLICY t_group_data_read
ON public."t_Group_Data"
FOR SELECT
TO anon, authenticated
USING (true);

DROP POLICY IF EXISTS t_group_data_manage_service ON public."t_Group_Data";
CREATE POLICY t_group_data_manage_service
ON public."t_Group_Data"
AS PERMISSIVE
FOR ALL
TO service_role
USING (true)
WITH CHECK (true);

REVOKE ALL ON TABLE public."t_Group_Data" FROM anon, authenticated;
GRANT SELECT ON TABLE public."t_Group_Data" TO anon, authenticated;
GRANT ALL ON TABLE public."t_Group_Data" TO service_role;

DO $$
BEGIN
  IF to_regclass('public."t_Group_Data_id_seq"') IS NOT NULL THEN
    EXECUTE 'GRANT USAGE, SELECT ON SEQUENCE public."t_Group_Data_id_seq" TO service_role';
  END IF;
END
$$;

GRANT EXECUTE ON PROCEDURE public.insert_24pvgroup_data() TO service_role;
GRANT EXECUTE ON PROCEDURE public.insert_23dsgroup_data() TO service_role;

COMMIT;
