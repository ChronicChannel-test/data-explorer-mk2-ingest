      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2008) AS f2008,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2009) AS f2009,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2010) AS f2010,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2011) AS f2011,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2012) AS f2012,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 2013) AS f2013,
