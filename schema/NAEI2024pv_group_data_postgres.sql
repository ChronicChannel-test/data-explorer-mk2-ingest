      f2010, f2011, f2012, f2013, f2014, f2015, f2016, f2017, f2018, f2019,
      f2020, f2021, f2022, f2023, f2024
    )
    SELECT
      g.id,
      sv.pollutant_id,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1970) AS f1970,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1971) AS f1971,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1972) AS f1972,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1973) AS f1973,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1974) AS f1974,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1975) AS f1975,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1976) AS f1976,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1977) AS f1977,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1978) AS f1978,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1979) AS f1979,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1980) AS f1980,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1981) AS f1981,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1982) AS f1982,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1983) AS f1983,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1984) AS f1984,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1985) AS f1985,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1986) AS f1986,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1987) AS f1987,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1988) AS f1988,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1989) AS f1989,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1990) AS f1990,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1991) AS f1991,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1992) AS f1992,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1993) AS f1993,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1994) AS f1994,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1995) AS f1995,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1996) AS f1996,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1997) AS f1997,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1998) AS f1998,
      SUM(sv.metric_value) FILTER (WHERE sv.reporting_year = 1999) AS f1999,
