CREATE OR REPLACE PROCEDURE public.insert_24pvgroup_data()
LANGUAGE plpgsql
AS $$
DECLARE
  g record;
BEGIN
  CREATE TEMP TABLE tmp_group_buffer (
    "Group_id" integer NOT NULL,
    "Pollutant_id" bigint NOT NULL,
    f1970 numeric(43,22),
    f1971 numeric(43,22),
    f1972 numeric(43,22),
    f1973 numeric(43,22),
    f1974 numeric(43,22),
    f1975 numeric(43,22),
    f1976 numeric(43,22),
    f1977 numeric(43,22),
